    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.reveal').forEach(el => {
        observer.observe(el);
    });

    // --- Theme Toggle Logic ---
    const themeToggle = document.getElementById('theme-toggle');
    const htmlElement = document.documentElement;

    function initTheme() {
        const savedTheme = localStorage.getItem('theme') || (window.matchMedia('(prefers-color-scheme: light)').matches ? 'light' : 'dark');
        htmlElement.setAttribute('data-theme', savedTheme);
    }

    function toggleTheme() {
        const currentTheme = htmlElement.getAttribute('data-theme');
        const newTheme = currentTheme === 'light' ? 'dark' : 'light';
        
        htmlElement.setAttribute('data-theme', newTheme);
        localStorage.setItem('theme', newTheme);
    }

    if (themeToggle) {
        themeToggle.addEventListener('click', toggleTheme);
    }

    initTheme();

    // --- Language Switching Logic ---
    const langBtns = document.querySelectorAll('.lang-btn');
    
    function setLanguage(lang) {
        // Update HTML lang and data-lang attributes
        htmlElement.setAttribute('lang', lang === 'pt' ? 'pt-BR' : (lang === 'en' ? 'en-US' : 'es-ES'));
        htmlElement.setAttribute('data-lang', lang);
        
        // Update all translatable elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                el.innerHTML = translations[lang][key];
            }
        });

        // Update all translatable tooltips/titles
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            if (translations[lang] && translations[lang][key]) {
                el.setAttribute('title', translations[lang][key]);
            }
        });

        // Update active class on buttons
        langBtns.forEach(btn => {
            btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
        });

        // Update modal content if open
        const modal = document.getElementById('project-modal');
        if (modal && modal.classList.contains('show')) {
            const activeProjectId = modal.getAttribute('data-active-project');
            if (activeProjectId) {
                fillModalContent(activeProjectId, lang);
            }
        }

        // Save to localStorage
        localStorage.setItem('language', lang);
    }

